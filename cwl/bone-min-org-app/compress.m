function [Ic] = compress(I)
%
W = sqrt(2) ;
%
Ic = [ 1*I(1,1,1,1) 1*I(1,1,2,2) 1*I(1,1,3,3) ...
    W*I(1,1,2,3) W*I(1,1,1,3) W*I(1,1,1,2) ; ...
    1*I(2,2,1,1) 1*I(2,2,2,2) 1*I(2,2,3,3) ...
    W*I(2,2,2,3) W*I(2,2,1,3) W*I(2,2,1,2) ; ...
    1*I(3,3,1,1) 1*I(3,3,2,2) 1*I(3,3,3,3) ...
    W*I(3,3,2,3) W*I(3,3,1,3) W*I(3,3,1,2) ; ...
    W*I(2,3,1,1) W*I(2,3,2,2) W*I(2,3,3,3) ...
    2*I(2,3,2,3) 2*I(2,3,1,3) 2*I(2,3,1,2) ; ...
    W*I(1,3,1,1) W*I(1,3,2,2) W*I(1,3,3,3) ...
    2*I(1,3,2,3) 2*I(1,3,1,3) 2*I(1,3,1,2) ; ...
    W*I(1,2,1,1) W*I(1,2,2,2) W*I(1,2,3,3) ...
    2*I(1,2,2,3) 2*I(1,2,1,3) 2*I(1,2,1,2) ] ;
%
