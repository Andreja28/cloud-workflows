% P3333 for spherical inclusions
% in transversely isotropic matrix
%----------------------------------
function y=fun_Psph3333(x,C1111,C1122,C1133,C3333,C2323);
y=1./2.*x.^2.*(x.^2.*C2323-C1111.*x.^2+C1111)./...
    (2.*C2323.*x.^4.*C1133+C2323.*x.^4.*C3333+C1111.*x.^4.*C2323-...
    2.*C2323.*x.^2.*C1133-2.*C1111.*C2323.*x.^2+C1111.*C2323+...
    x.^4.*C1133.^2-C1111.*x.^4.*C3333-x.^2.*C1133.^2+C1111.*x.^2.*C3333);
