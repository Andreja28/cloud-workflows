function[Psph]=P_isotrans_sph(C0)

P1111=quad('fun_Psph_1111',-1,1,[],[],C0(1,1),C0(1,2),C0(1,3),C0(3,3),C0(4,4)/2);
P1122=quad('fun_Psph_1122',-1,1,[],[],C0(1,1),C0(1,2),C0(1,3),C0(3,3),C0(4,4)/2);
P1133=quad('fun_Psph_1133',-1,1,[],[],C0(1,1),C0(1,2),C0(1,3),C0(3,3),C0(4,4)/2);
P3333=quad('fun_Psph_3333',-1,1,[],[],C0(1,1),C0(1,2),C0(1,3),C0(3,3),C0(4,4)/2);
P2323=quad('fun_Psph_2323',-1,1,[],[],C0(1,1),C0(1,2),C0(1,3),C0(3,3),C0(4,4)/2);

Psph=zeros(6,6);

Psph(1,1)=P1111;
Psph(2,2)=Psph(1,1);
Psph(3,3)=P3333;
Psph(1,2)=P1122;
Psph(1,3)=P1133;
Psph(2,3)=Psph(1,3);
Psph(2,1)=Psph(1,2);
Psph(3,1)=Psph(1,3);
Psph(3,2)=Psph(2,3);
Psph(4,4)=2*P2323;
Psph(5,5)=Psph(4,4);
Psph(6,6)=2*0.5*(P1111-P1122);


